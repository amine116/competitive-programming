#include<bits/stdc++.h>
#define ll long long
using namespace std;
int main(){
    ll n, s, count = 0; cin >> n >> s;
    ll arr[n + 1];
    for(ll i = 0; i < n; i++) cin >> arr[i];
    sort(arr, arr+n);
    ll mid = n/2;
    for(ll i = 0; i < n; i++){
        if(i <= mid && arr[i] > s) count += (arr[i] - s);
        if(i >= mid && arr[i] < s) count += (s - arr[i]);
    }
    cout << count << endl;
    return 0;
}
